// Draw "AP" label above the icon using the built-in 5x7 GFX font (no extra include needed)
inline void _drawAPLabel(int16_t x, int16_t y) {
    if (WiFi.getMode() & WIFI_AP) {
        Display.drawText(x + 1, y - 2, "AP", C_ORANGE, nullptr);
    }
}

